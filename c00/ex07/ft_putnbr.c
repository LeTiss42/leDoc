#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	res[100];
	int		i;

	i = 0;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{	
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = -nb;
		}
		if (nb == 0)
			write(1, "0", 1);
		while (nb > 0)
		{
			res[i++] = ('0' + (nb % 10));
			nb = nb / 10;
		}
		while (i > 0)
			write(1, &res[--i], 1);
	}
}
