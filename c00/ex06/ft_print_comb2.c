#include <unistd.h>

void	ft_print_comb2(void)
{
	char	tot[5];
	int		a;
	int		b;

	a = 0;
	while (a < 99)
	{
		b = a + 1;
		while (b < 100)
		{
			tot[0] = '0' + a / 10;
			tot[1] = '0' + a % 10;
			tot[2] = ' ';
			tot[3] = '0' + b / 10;
			tot[4] = '0' + b % 10;
			write(1, tot, 5);
			if (a < 98)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
